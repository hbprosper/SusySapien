//--------------------------------------------------------------------
class jetdata
{
public:
  jetdata();
  ~jetdata() {}

  double operator()(std::vector<double>& x);
  double operator()(double x, std::string v);
  double density(std::vector<double>& x);
  double density(double x, std::string v);
  double integral(std::vector<double>& a, std::vector<double>& b);
  std::vector<double> bandwidth();

private:
  std::map<std::string, int> _vmap;
};

jetdata::jetdata()
  : _vmap(std::map<std::string, int>())
{
  for(int i=0; i < ndim; i++) _vmap[v[i]] = i;
}

double jetdata::operator()(vector<double>& x)
{
  if ( x.size() != (unsigned)ndim ) return -1;
  double p = 0;
  for(int i=0; i < ndata; i++)
    {
      double zz = 0;
      for(int j=0; j < ndim; j++)
        {
          double y = x[j] - data[i][j];
          zz += hh[j] * y * y;
        }
      p += exp(-zz);
    }
  return p / NORM;
}
//--------------------------------------------------------------------
double jetdata::operator()(double x, std::string var)
{
  if ( _vmap.find(var) == _vmap.end() ) return -1;
  int dimension = _vmap[var];
  if ( (dimension < 0) || (dimension > ndim-1) ) return -1;
  double p = 0;
  for(int i=0; i < ndata; i++)
    {
      double q = 1;
      for(int j=0; j < ndim; j++)
        {
          if ( j == dimension )
            {
              double z = (x - data[i][j])/h[j];
              q *= exp(-0.5*z*z) / (h[j]*root2pi);
            }
          else
            {
              double za = (lower[j]-data[i][j])/h[j];
              double zb = (upper[j]-data[i][j])/h[j];
              q *= TMath::Freq(zb)-TMath::Freq(za);
            }
        }
      p += q;
    }
  return p / (norm * ndata);
}
double jetdata::density(vector<double>& x) {return (*this)(x);}
double jetdata::density(double x, std::string var){return (*this)(x, var);}
//--------------------------------------------------------------------
double jetdata::integral(vector<double>& a, vector<double>& b)
{
  double p = 0;
  for(int i=0; i < ndata; i++)
    {
      double q = 1;
      for(int j=0; j < ndim; j++)
        {
          double za = (a[j]-data[i][j])/h[j];
          double zb = (b[j]-data[i][j])/h[j];
          q *= TMath::Freq(zb)-TMath::Freq(za);
        }
      p += q;
    }
  return p / (norm * ndata);
}
//--------------------------------------------------------------------
vector<double> jetdata::bandwidth()
{
  vector<double> v(ndim);
  copy(h, h + ndim, v.begin());
  return v;
}
